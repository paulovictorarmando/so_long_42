#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*s1;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	s1 = ft_calloc(len + 1, sizeof(char));
	if (!s1)
		return (NULL);
	ft_strlcpy(s1, s + start, len + 1);
	return (s1);
}
